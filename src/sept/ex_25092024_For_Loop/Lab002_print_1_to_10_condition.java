package sept.ex_25092024_For_Loop;

public class Lab002_print_1_to_10_condition {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            if (i == 5) {
                System.out.println("Five");

            }
            System.out.println(i);
        }

    }

}
